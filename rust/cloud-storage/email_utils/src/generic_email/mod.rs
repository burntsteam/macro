lazy_static! {
    static ref HEX_PATTERN: Regex = Regex::new(r"^[a-f0-9\-]{20,}$").unwrap();
    static ref UUID_PATTERN: Regex =
        Regex::new(r"^[a-f0-9]{8}-[a-f0-9]{4}-[a-f0-9]{4}-[a-f0-9]{4}-[a-f0-9]{12}$").unwrap();
}

/// Checks if an email address is likely a generic/automated system email
///
/// This function identifies common patterns in email addresses that typically
/// indicate automated senders rather than individual users.
pub fn is_generic_email(email: &str) -> bool {
    let email_lower = email.to_lowercase();

    // Split into local part and domain
    let (local_part, domain) = match email_lower.split_once('@') {
        Some(parts) => parts,
        None => return false, // Invalid email
    };

    // Check for automated email service domains first (most definitive)
    if is_automated_domain(domain) {
        return true;
    }

    // Check for obvious automated patterns in local part
    if contains_automated_keyword(local_part) {
        return true;
    }

    // Check if local part starts with automated prefix
    if starts_with_automated_prefix(local_part) {
        return true;
    }

    // Check for auto-generated local parts (numbers, UUIDs, hex strings)
    if is_autogenerated_local_part(local_part) {
        return true;
    }

    false
}

