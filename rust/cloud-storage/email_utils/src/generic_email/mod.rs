fn is_autogenerated_local_part(local_part: &str) -> bool {
    // Too long - likely autogenerated
    if local_part.len() > 50 {
        return true;
    }

    // UUID pattern
    if UUID_PATTERN.is_match(local_part) {
        return true;
    }

    // Long hex string pattern
    if HEX_PATTERN.is_match(local_part) {
        return true;
    }

    // Purely numeric with delimiters (e.g., "123456", "123-456-789")
    if !local_part.is_empty()
        && local_part
            .chars()
            .all(|c| c.is_ascii_digit() || c == '.' || c == '-' || c == '_')
        && local_part.chars().any(|c| c.is_ascii_digit())
    {
        return true;
    }

    // Starts with "id" followed by only numbers (e.g., "id12345")
    if local_part.starts_with("id")
        && local_part.len() > 2
        && local_part[2..].chars().all(|c| c.is_ascii_digit())
    {
        return true;
    }

    // Starts with numbers followed by random-looking characters (e.g., "0000012345abcdef")
    let digit_prefix_count = local_part
        .chars()
        .take_while(|c| c.is_ascii_digit())
        .count();
    if digit_prefix_count >= 6 {
        return true;
    }

    false
}

/// Deduplicates a list of email addresses by normalizing them (removing + aliases)
pub fn dedupe_emails(emails: Vec<String>) -> Vec<String> {
    emails
        .into_iter()
        .map(|mut email| {
            // Remove everything from + to @ if + exists
            if let (Some(plus_pos), Some(at_pos)) = (email.find('+'), email.find('@'))
                && plus_pos < at_pos
            {
                email.drain(plus_pos..at_pos);
            }

            email
        })
        .collect::<std::collections::HashSet<_>>()
        .into_iter()
        .collect()
}
